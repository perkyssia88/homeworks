# Просим ввести дату рождения
date = int(input("Enter your date of birth: "))
# Просим ввести месяц рождения
month = int(input("Enter the month of your birth as a number: "))
# List  со знаками зодиака
zodiac_sings = ["козерог", "водолей", "рыбы", "овен", "телец", "близнецы",
                "рак", "лев", "дева", "весы", "скорпион", "стрелец"]
# создаем условие если дата рождения больше или равна 1 и меньше или равна 20
if 1 <= date <= 20:
    # выводим на печать знак зодиака из list под индексом месяца рождения - 1
    # т.к. индексация начинается с 0
    print(f"По знаку зодиака вы - {zodiac_sings[month - 1]}")
# если дата рождения больше или равна 21 и меньше или равна 31
elif 21 <= date <= 31:
    # выводим на экран знак зодиака из list под индексом месяца рождения
    print(f"По знаку зодиака вы - {zodiac_sings[month]}")
# если не выполнено ни одно условие - выводим некорректный ввод
else:
    print("Enter correct date")

