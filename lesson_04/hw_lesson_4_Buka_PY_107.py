# Ex_3
# запрашиваем пользовательский ввод и переводим из str в int
month = int(input("Введите номер месяца: "))
# создаем условие оператором if
if 3 <= month <= 5:
    # если месяц 3, 4 или 5 - весна, выводим на печать
    print("Spring")
# если месяц 6, 7 или 8 - лето, в печать
elif 6 <= month <= 8:
    print("Summer")
# если месяц 9, 10 или 11 - осень, в печать
elif 9 <= month <= 11:
    print("Autumn")
# если месяц 12 или 1, 2 - зима, в печать
elif month == 12 or 1 <= month <= 2:
    print("Winter")
# если не одно условие не было выполнено
else:
    # вывод в печать не верный номер месяца
    print("Wrong month number")

# Ex_8
print(f"Программа простейший калькулятор.\n"
      f"Возможные производимые вычисления: '+', '-', '*', '**', '%', '/', '//'\n"
      f"Дробные числа вводятся через точку: прим. '3.14'")
# запрашиваем первую цифру - переводим из str во float
num_1 = Decimal(input("Введите число: "))
# запрашиваем символ математического знака
char = input("Введите математический знак: ")
# запрашиваем вторую цифру - переводим из str во float
num_2 = Decimal(input("Введите число: "))
# если символ + производим сложение и выводим на экран
if char == "+":
    print(f"Cумма чисел {num_1} и {num_2} равна {num_1 + num_2}")
# если символ - производим вычитание и выводим на экран
elif char == "-":
    print(f"Разность чисел {num_1} и {num_2} равна {num_1 - num_2}")
# если символ * производим умножение и выводим на экран
elif char == "*":
    print(f"Произведение чисел {num_1} и {num_2} равна {num_1 * num_2}")
# если символ ** производим возведение в степень и выводим на экран
elif char == "**":
    print(f"Число {num_1} в квадрате {num_2} равно {num_1 ** num_2}")
# если символ % производим деление с остатком и проверяем чтобы делитель не был 0
# выводим на экран
elif char == "%" and num_2 != 0:
    print(f"Остаток от деления {num_1} на число {num_2} равен {num_1 % num_2}")
# если символ / производим деление и проверяем чтобы делитель не был 0
# выводим на экран
elif char == "/" and num_2 != 0:
    print(f"Деление числа {num_1} на число {num_2} равно {num_1 / num_2}")
# если символ // производим целочисленное деление и проверяем чтобы делитель не был 0
# выводим на экран
elif char == "//" and num_2 != 0:
    print(f"Целочисленное деление {num_1} на {num_2} равено {num_1 - num_2}")
# если символ %, / или //
# проверяем чтобы делитель не был 0, если True
# выводим на экран предупреждение о делении на 0
elif char == "/" or char == "//" or char == "%" and num_2 == 0:
    print(f"На {int(num_2)} делить нельзя")
# если не одно из условий не выполнено
else:
    # выводим на экран предупреждение о некорректных данных
    print("Вы ввели некоррекные данные")

